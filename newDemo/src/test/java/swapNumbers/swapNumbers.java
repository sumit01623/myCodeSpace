package swapNumbers;

public class swapNumbers {
	public static void main(String[] args) {
		int a = 10;
		int b = 100;
		int c = 200;
		int d;
		
		d = a;
		a = b;
		b = c;
		c = d;
		
		System.out.println("value of a is: "+a);
		System.out.println("value of b is: "+b);
		System.out.println("value of c is: "+c);
	}
	

}


